package com.heyplogging.domain.vo;

public class ProductVO {
	private int productNumber;
	private String productName; 
	private String productDetail; 
	private int productPrice; 
	private String productColor; 
	private String productSize; 
	private String productCategory; 
	private String productPopularity; 
	private int productReviewNumbers; 
	private int productInventoryTotal; 
	private int productInventorySold; 
	
	public ProductVO() {;}

	public int getProductNumber() {
		return productNumber;
	}

	public void setProductNumber(int productNumber) {
		this.productNumber = productNumber;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getProductDetail() {
		return productDetail;
	}

	public void setProductDetail(String productDetail) {
		this.productDetail = productDetail;
	}

	public int getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}

	public String getProductColor() {
		return productColor;
	}

	public void setProductColor(String productColor) {
		this.productColor = productColor;
	}

	public String getProductSize() {
		return productSize;
	}

	public void setProductSize(String productSize) {
		this.productSize = productSize;
	}

	public String getProductCategory() {
		return productCategory;
	}

	public void setProductCategory(String productCategory) {
		this.productCategory = productCategory;
	}

	public String getProductPopularity() {
		return productPopularity;
	}

	public void setProductPopularity(String productPopularity) {
		this.productPopularity = productPopularity;
	}

	public int getProductReviewNumbers() {
		return productReviewNumbers;
	}

	public void setProductReviewNumbers(int productReviewNumbers) {
		this.productReviewNumbers = productReviewNumbers;
	}

	public int getProductInventoryTotal() {
		return productInventoryTotal;
	}

	public void setProductInventoryTotal(int productInventoryTotal) {
		this.productInventoryTotal = productInventoryTotal;
	}

	public int getProductInventorySold() {
		return productInventorySold;
	}

	public void setProductInventorySold(int productInventorySold) {
		this.productInventorySold = productInventorySold;
	}

}
